public class Rocks {
    int n;
    int m;
    public Rocks(int n, int m){
        this.n = n;
        this.m = m;
    }

    public static boolean winOrLose(int n, int m){
        if(n % 2 == 0 && n % 2 == 0){
            return false;
        }
        return true;
    }
}
